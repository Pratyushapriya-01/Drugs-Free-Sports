    <h3>Summary</h3>
    <p>Hundreds of drugs and methods have been used in doping for the purpose of sports performance enhancement. While this practice has been around for centuries, it is still prevalent in sports competitions at all levels. While many methods are associated with increased performance, almost all methods are deleterious to the athlete’s health in some manner. Regulations and testing by many agencies, including WADA and the IOC aim to decrease the incidence of doping and eliminate its use in sport. However, given the importance placed on athletic achievement, the fame awarded to elite athletes in today’s culture, and the rapid development of new performance enhancing therapies, it is likely that doping will be a problem that will continue for many years to come.</p>
