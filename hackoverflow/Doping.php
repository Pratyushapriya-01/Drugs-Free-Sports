    <h2>Doping</h2>
    <h3>About Doping</h2>
    <p>
    The term <em>“doping”</em> refers to the use of prohibited medications, drugs, or treatments by athletes with the intention of improving athletic performance. The practice of doping by athletes dates back centuries. However it has recently received increased attention due to a wide variety of potential performance enhancing drugs that are now available, and also due to prominent cases of doping by elite athletes that have been reported in the media.
    </p>
    <h3> Effects of doping & Regulations</h3>
    <p>
    Despite the visible improvement in performance noted in countless athletes throughout several centuries, it was also noted that the athletes would often suffer adverse health effects and even premature death that seemed to be associated with the doping practices. These adverse outcomes and deaths lead to the first ban on doping by the International Association of Athletics Federation in 1928. 
    <br>
    <br>
    According to the World Anti-Doping Code, which was established by WADA in 2008, a substance or treatment constitutes doping if it meets two of the three following criteria:
    <span><br></span>
    <span>
    • It enhances performance
    </span>
    <span><br></span>
    <span>
    • It presents a risk to the athlete's health
    </span>
    <span><br></span>
    <span>
    • It is contrary to the spirit of the sport
    </span>
    <br> 
    <br>
    This code consists of a yearly-published list of prohibited substances and treatment methods that athletes are not allowed to use. Various penalties can be enforced upon athletes found to be in violation of the Anti-Doping Code. Many athletes found in violation of the code have previously suffered punishments ranging from being stripped of Olympic medals or sports titles to lifetime bans from future competition in sport.
    </p>
    <h3>Methods and Goals of Doping</h3>
    <p>
       The goals of doping practices typically fall into four broad categories: Anabolic Drugs (Anabolic Steroids),Stimulants,Hormones,Oxygen Transport (“Blood Doping”). These are substances that increase muscle mass, substances that decrease recovery time, substances that increase energy and/or endurance, and substances that mask the presence of other drugs.
